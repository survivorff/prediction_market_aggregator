/**
 * Repository (persistence) ports referenced by the design's ingestion and
 * matching algorithms (see design.md "Ingestion Pipeline Algorithms" and
 * "Same-Question Matching Engine").
 *
 * These are pure interface declarations: the I/O-free `@pma/core` package
 * defines the contract; concrete Postgres/TimescaleDB/Redis implementations
 * live in `packages/storage` (the dependency rule in design.md "Layered
 * Architecture"). The method signatures are designed to support:
 *
 * - idempotent upserts keyed on `(source_id, external_id)` (Requirements 7.1,
 *   10.1) and `(market_id, outcome_id, ts)` (Requirements 7.2, 10.2);
 * - keyset cursor load/save for crash-safe resume (Requirement 7.3);
 * - cross-platform candidate search and canonical linking for the matching
 *   engine (Requirement 11.x).
 */

/** Which keyset cursor stream a {@link CursorRepository} call addresses. */
export type CursorEntity = "event" | "market";

/**
 * Input shape for {@link MarketRepository.upsertMarket}. The internal `id` is
 * assigned/resolved by the persistence layer via the `(sourceId, externalId)`
 * idempotency key, so callers supply everything except `id`.
 */
export type MarketUpsert = Omit<Market, "id">;

/**
 * Input shape for {@link OutcomeRepository.upsertOutcome}. The internal `id` is
 * resolved by the persistence layer via the `(marketId, label)` key.
 */
export type OutcomeUpsert = Omit<Outcome, "id">;

/**
 * Input shape for {@link CanonicalEventRepository.create}. The internal `id`
 * is generated by the persistence layer.
 */
export type CanonicalEventInput = Omit<CanonicalEvent, "id">;

/**
 * A persisted {@link Market} augmented with the matching-engine mismatch flag.
 *
 * The normalized domain `Market` (design.md "Model Definitions") intentionally
 * omits `resolutionMismatch`; the flag is set by matching Layer 4 and stored on
 * the `market` row (`resolution_mismatch`, design.md "Storage Schemas"). Ports
 * that drive matching/signals expose it via this shape so the core model stays
 * pure. Markets with `resolutionMismatch = true` are excluded from spread
 * signals (Requirements 3.2, 11.3).
 */
export interface LinkedMarket extends Market {
  resolutionMismatch: boolean;
}

/**
 * Stores and retrieves keyset pagination cursors for crash-safe, incremental
 * metadata sync. Cursors are keyed by `(sourceId, entity)` per the
 * `sync_cursor` table (design.md "Storage Schemas") and SHALL never regress
 * across successful syncs (Requirement 7.3).
 */
export interface CursorRepository {
  /** Load the persisted cursor for a source + entity stream; `null` = start. */
  loadCursor(sourceId: string, entity: CursorEntity): Promise<string | null>;
  /**
   * Persist the cursor for a source + entity stream. Called only after the
   * corresponding page is durably written; `null` resets to the stream start.
   */
  saveCursor(sourceId: string, entity: CursorEntity, cursor: string | null): Promise<void>;
}

/**
 * Persistence for normalized markets plus the market-scoped keyset cursor used
 * by `syncMarkets` (design.md). `loadCursor`/`saveCursor` here are the
 * convenience, market-entity views of {@link CursorRepository}; an
 * implementation may satisfy both ports.
 *
 * `upsertMarket` is idempotent on `(sourceId, externalId)`: re-running with the
 * same upstream state produces no duplicate rows and no net change (Requirement
 * 7.1 / Property 1).
 */
export interface MarketRepository {
  /** Load the market-sync keyset cursor for a source; `null` = start. */
  loadCursor(sourceId: string): Promise<string | null>;
  /** Persist the market-sync cursor, only after the page is durably written. */
  saveCursor(sourceId: string, cursor: string | null): Promise<void>;

  /**
   * Idempotent upsert keyed on `(sourceId, externalId)`
   * (`ON CONFLICT (source_id, external_id) DO UPDATE`). Returns the persisted
   * market with its resolved internal `id`.
   *
   * The optional `category` projects the adapter's category hint onto the
   * denormalized `market.category` column (the domain `Market` carries no
   * category). When omitted, a new row defaults to `"other"` and an existing
   * row's category is preserved (so a category set by a later layer survives an
   * idempotent re-sync).
   */
  upsertMarket(market: MarketUpsert, category?: Category): Promise<Market>;

  /** Resolve a market by its idempotency key; `null` when not present. */
  findByExternalId(sourceId: string, externalId: string): Promise<Market | null>;

  /** Fetch a market by internal id; `null` when not present. */
  getById(id: string): Promise<Market | null>;
}

/**
 * Persistence for market outcomes. `upsertOutcome` is idempotent on
 * `(marketId, label)` per the `outcome` table's unique constraint (design.md
 * "Storage Schemas").
 */
export interface OutcomeRepository {
  /** Idempotent upsert keyed on `(marketId, label)`; returns the persisted row. */
  upsertOutcome(outcome: OutcomeUpsert): Promise<Outcome>;
  /** Batch idempotent upsert; order of the returned rows matches the input. */
  upsertOutcomes(outcomes: readonly OutcomeUpsert[]): Promise<Outcome[]>;
  /** List a market's outcomes. */
  listByMarket(marketId: string): Promise<Outcome[]>;
}

/**
 * Time-series persistence for the `price_point` TimescaleDB hypertable.
 *
 * `writePricePoint` is idempotent on `(marketId, outcomeId, ts)` so reconnect
 * backfill overlapping live ticks yields exactly one row (Requirement 7.2 /
 * Property 2). `history` backs price-curve queries and WebSocket gap backfill
 * (Requirements 4.2, 4.4, 7.6).
 */
export interface PricePointRepository {
  /** Idempotent append keyed on `(marketId, outcomeId, ts)`. */
  writePricePoint(point: PricePoint): Promise<void>;
  /** Batch idempotent append; duplicates/reorderings collapse to one row each. */
  writePricePoints(points: readonly PricePoint[]): Promise<void>;
  /**
   * Read a continuous price series for a market over a time range (and optional
   * downsampling interval) for curves and reconnect backfill.
   */
  history(marketId: string, range: TimeRange): Promise<PricePoint[]>;
  /** Latest stored point for a market outcome; `null` when none exists. */
  latest(marketId: string, outcomeId: string): Promise<PricePoint | null>;
}

/**
 * Persistence for cross-platform {@link CanonicalEvent} groupings. Linking of
 * markets to a canonical event is handled by {@link MatchingRepository} so the
 * matching algorithm's vocabulary stays cohesive.
 */
export interface CanonicalEventRepository {
  /** Create a canonical event; the persistence layer generates its `id`. */
  create(canonical: CanonicalEventInput): Promise<CanonicalEvent>;
  /** Fetch a canonical event by id; `null` when not present. */
  getById(id: string): Promise<CanonicalEvent | null>;
  /** List canonical events for a category (matching Layer-1 candidate scoping). */
  listByCategory(category: Category): Promise<CanonicalEvent[]>;
}

/** A time window (ISO 8601 bounds) used to scope candidate search. */
export interface TimeWindow {
  /** ISO 8601 inclusive lower bound. */
  from: string;
  /** ISO 8601 inclusive upper bound. */
  to: string;
}

/**
 * Layer-1 candidate-search query (design.md `matchMarket`). Scopes the pool by
 * category, a time window around the candidate's end date, and the extracted
 * subject entity / numeric threshold (`null` when not extractable).
 */
export interface CandidateQuery {
  category: Category;
  /** Time window around the candidate's end date. */
  timeWindow: TimeWindow;
  /** Extracted subject entity (e.g. "BTC"); `null` when none. */
  subjectEntity: string | null;
  /** Extracted numeric threshold (e.g. 100000); `null` when none. */
  threshold: number | null;
  /** Optional market id to exclude from the pool (the candidate itself). */
  excludeMarketId?: string;
}

/** Options for {@link MatchingRepository.linkToCanonical}. */
export interface CanonicalLinkOptions {
  /**
   * When `true`, the two markets settle by materially different resolution
   * criteria; both rows are flagged `resolutionMismatch = true` and excluded
   * from spread signals (Requirements 11.3, 3.2 / design.md matching Layer 4).
   */
  mismatch: boolean;
}

/**
 * Candidate-finding and canonical-linking port for the same-question matching
 * engine (design.md `matchMarket` / `computeSignals`).
 *
 * Linkage is symmetric: linking A to B places both in the same canonical event,
 * so membership queried via {@link marketsForCanonical} is order-independent
 * (Requirement 2.2 / Property 9).
 */
export interface MatchingRepository {
  /**
   * Layer-1 candidate search: return markets matching the metadata pre-filter
   * (category + time window + subject entity + threshold).
   */
  findCandidates(query: CandidateQuery): Promise<Market[]>;

  /**
   * Link two markets to a shared canonical event (creating one if neither is
   * linked yet), applying the resolution-mismatch flag from `options`. Returns
   * the canonical event the pair now belongs to.
   */
  linkToCanonical(
    marketA: Market,
    marketB: Market,
    options: CanonicalLinkOptions,
  ): Promise<CanonicalEvent>;

  /**
   * Return all markets linked to a canonical event, each carrying its
   * `resolutionMismatch` flag so signal computation can exclude mismatched and
   * non-open markets (Requirements 3.2, 3.4).
   */
  marketsForCanonical(canonicalEventId: string): Promise<LinkedMarket[]>;
}

/**
 * Input shape for {@link WatchlistRepository.add}: a user-scoped pointer to a
 * tracked target. The internal `id` and `createdAt` are assigned by the
 * persistence layer; duplicates per `(userId, targetType, targetId)` are
 * prevented there (Requirement 5.1).
 */
export interface WatchlistItemInput {
  userId: string;
  targetType: WatchlistTargetType;
  targetId: string;
}

/**
 * Persistence for user-scoped watchlist entries (design.md "Component 6: Alert
 * / Watchlist Service" + the `watchlist_item` table). All operations are
 * scoped to a `userId` so a user can only ever read or delete their OWN items
 * (Requirements 5.4, 9.4); cross-user access is impossible through this port.
 *
 * `add` is idempotent on `(userId, targetType, targetId)`: re-adding the same
 * target returns the existing entry rather than creating a duplicate row
 * (Requirement 5.1).
 */
export interface WatchlistRepository {
  /**
   * Add a target to a user's watchlist, or return the existing entry when the
   * same `(userId, targetType, targetId)` is already present — no duplicate row
   * is created (Requirement 5.1). Returns the persisted (existing or created)
   * {@link WatchlistItem}.
   */
  add(input: WatchlistItemInput): Promise<WatchlistItem>;

  /** List a user's watchlist entries (newest first). User-scoped (Req 9.4). */
  listByUser(userId: string): Promise<WatchlistItem[]>;

  /**
   * Fetch a single watchlist entry by id, scoped to its owner. Returns `null`
   * when the id does not exist OR is not owned by `userId` (so callers cannot
   * probe other users' items) — supports the gateway's ownership/404 check
   * (Requirements 5.4, 9.4).
   */
  getById(userId: string, itemId: string): Promise<WatchlistItem | null>;

  /**
   * Delete a user's watchlist entry by id, scoped to its owner. Returns `true`
   * when a row was removed, `false` when no matching `(userId, id)` row existed
   * (unknown id OR owned by another user → the gateway maps `false` to 404).
   * Stops the system from evaluating it (Requirement 5.4).
   */
  delete(userId: string, itemId: string): Promise<boolean>;
}

/**
 * Input shape for {@link AlertRuleRepository.create}: a user-scoped alert rule.
 * The internal `id`, `createdAt`, and the default `active` flag are assigned by
 * the persistence layer (so `active` is optional here, defaulting to `true` per
 * the `alert_rule.active DEFAULT TRUE` column). `params` must match `ruleType`
 * (validated before insert — Requirement 5.2).
 */
export interface AlertRuleInput {
  userId: string;
  targetType: WatchlistTargetType;
  targetId: string;
  ruleType: AlertRuleType;
  params: AlertRuleParams;
  /** Whether the rule starts active; defaults to `true` when omitted. */
  active?: boolean;
}

/**
 * Persistence for user-scoped alert rules (design.md "Component 6: Alert /
 * Watchlist Service" + the `alert_rule` table). All operations are scoped to a
 * `userId` so a user can only ever read or delete their OWN rules
 * (Requirements 5.4, 9.4); cross-user access is impossible through this port.
 *
 * Unlike {@link WatchlistRepository.add}, `create` is NOT deduplicated — a user
 * may create multiple rules for the same target (e.g. different thresholds), so
 * each call inserts a new row (Requirement 5.2).
 */
export interface AlertRuleRepository {
  /**
   * Persist a new alert rule with its parameters and `active` flag
   * (Requirement 5.2). Always inserts a new row (no dedup). Returns the
   * persisted {@link AlertRule} with its resolved internal `id` + `createdAt`.
   */
  create(input: AlertRuleInput): Promise<AlertRule>;

  /** List a user's alert rules (newest first). User-scoped (Req 9.4). */
  listByUser(userId: string): Promise<AlertRule[]>;

  /**
   * Fetch a single alert rule by id, scoped to its owner. Returns `null` when
   * the id does not exist OR is not owned by `userId` (so callers cannot probe
   * other users' rules) — supports the gateway's ownership/404 check
   * (Requirements 5.4, 9.4).
   */
  getById(userId: string, id: string): Promise<AlertRule | null>;

  /**
   * Delete a user's alert rule by id, scoped to its owner. Returns `true` when
   * a row was removed, `false` when no matching `(userId, id)` row existed
   * (unknown id OR owned by another user → the gateway maps `false` to 404).
   * Stops the system from evaluating it (Requirement 5.4).
   */
  delete(userId: string, id: string): Promise<boolean>;
}
